// Problem: Read a user's age and name, then display the details in the required format.
// Approach: Take the age and name as input and print them with appropriate labels.
// Time: O(1), Space: O(1)

#include<bits/stdc++.h>
using namespace std;    
int main(){
    int age;
    string name;
    cin >>age>>name;
    cout <<"Age: "<<age<<endl;
    cout <<"Name: "<<name<<endl;
    return 0;
}